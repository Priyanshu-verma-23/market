    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String n="";
        try{
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/ONLINE_SHOP","root","");
            Statement stmt=con.createStatement();
            n=NM.getText();
            int p=Integer.parseInt(CT.getText());
            String un=L1.getText().toString();
            String sql="select  * from booking where iname='"+n+"' and uname='"+un+"' and status='PENDING';";
            ResultSet rs=stmt.executeQuery(sql);
            int c=0;
            while(rs.next()) {
                c++;
            }
            if(c==0)
            {
                sql="Insert into booking Values('"+un+"','"+n+"',"+(p)+",'PENDING')";
                stmt.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Item added to CART !");
                NM.setText("");
                CT.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Item already in CART !!!!!");
                NM.setText("");
                CT.setText("");
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }    
}//GEN-LAST:event_jButton2ActionPerformed

