    private void WO(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_WO
        // TODO add your handling code here:
        DefaultTableModel tm = (DefaultTableModel) tb1.getModel();
          try {
            Class.forName("java.sql.Driver");
            String database = "jdbc:mysql://localhost/ONLINE_SHOP";
            Connection conn = DriverManager.getConnection(database, "root", "");
            Statement stmt = conn.createStatement();
            String sql = "select * from ITEMS ORDER BY IT_NAME ASC";
            ResultSet r=stmt.executeQuery(sql);
            Object[] newrow = new Object[3];
            while(r.next()) {
                newrow[0]=r.getString(1);
                newrow[1]=r.getString(2);
                newrow[2]=r.getString(3);
                tm.addRow(newrow);
            }
            stmt.close();
            conn.close();
        } catch (Exception e) { JOptionPane.showMessageDialog(null,e.getMessage());}
    }//GEN-LAST:event_WO

